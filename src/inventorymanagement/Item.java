    private void remove_itemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_remove_itemActionPerformed

        try {

            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            int selectIndex = jTable2.getSelectedRow();

            String code = model.getValueAt(selectIndex, 0).toString();

            String sql = "DELETE FROM item WHERE item_code=?";
            PreparedStatement pstmt = connection.prepareStatement(sql);

            pstmt.setInt(1, Integer.parseInt(code));
            pstmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Successfully Deleted");
            showTable();
        } catch (SQLException ex) {
            System.out.println(ex);
        }
    }//GEN-LAST:event_remove_itemActionPerformed

