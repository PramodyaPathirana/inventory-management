    public static ArrayList getAllItems() {

        ArrayList itemsList = new ArrayList();
        try {
            String sql = "SELECT * FROM item";
            Statement stmt = connection.createStatement();

            ResultSet res_st = stmt.executeQuery(sql);
            while (res_st.next()) {
                ItemModel singleItem = new ItemModel(
                        res_st.getInt(1),
                        res_st.getString(2),
                        res_st.getDouble(3),
                        res_st.getInt(4),
                        res_st.getDate(5),
                        res_st.getTime(6));
                itemsList.add(singleItem);
            }

        } catch (SQLException ex) {
            System.out.println(ex);
        }
        return itemsList;
    }

    public void showTable() {
        ArrayList<ItemModel> showlist = getAllItems();
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        model.setNumRows(0);
        Object[] row = new Object[6];
        for (int i = 0; i < showlist.size(); i++) {
            row[0] = showlist.get(i).getItemCode();
            row[1] = showlist.get(i).getItemName();
            row[2] = showlist.get(i).getUnitPrice();
            row[3] = showlist.get(i).getItemQuantity();
            row[4] = showlist.get(i).getAddedDate();
            row[5] = showlist.get(i).getAddedTime();
            System.out.println(row);
            model.addRow(row);
        }
    }


