    public static ArrayList getAllItems() {

        ArrayList staffList = new ArrayList();
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/inventory","root","");
            String sql = "SELECT * FROM staff";
            Statement stmt = con.createStatement();  

            ResultSet res_st = stmt.executeQuery(sql);
            
            staffModel staffmodel;
            while (res_st.next()) {
                staffmodel = new staffModel(res_st.getInt("staff_id"),res_st.getString("staff_name"),res_st.getString("staff_address"),res_st.getInt("staff_tel"),res_st.getString("priviledge"));
                 staffList.add(staffmodel);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        return staffList;
    }

    public void showTable() {
        ArrayList<staffModel> showlist = getAllItems();
        DefaultTableModel model = (DefaultTableModel) Table_Staff.getModel();
        Object[] row = new Object[5];
        model.setNumRows(0);
        for (int i = 0; i < showlist.size(); i++) {
            row[0] = showlist.get(i).getsno();
            row[1] = showlist.get(i).getname();
            row[2] = showlist.get(i).getaddress();
            row[3] = showlist.get(i).gettelNo();
            row[4] = showlist.get(i).getprivilege();
            System.out.println(row);
            model.addRow(row);
        }
    }

