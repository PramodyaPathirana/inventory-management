    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        String searchId = search_staff.getText();
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/inventory", "root", "");
            String sql = "SELECT staff_id, staff_name, staff_address,staff_tel, priviledge FROM staff WHERE staff_id ='" + searchId + "' ";

            Statement pstmt = con.createStatement();

            ResultSet rs = pstmt.executeQuery(sql);

            if (rs.next()) {
                staffId.setText(rs.getString("staff_id"));
                staffName.setText(rs.getString("staff_name"));
                address.setText(rs.getString("staff_address"));
                telNo.setText(rs.getString("staff_tel"));
                privilege.setText(rs.getString("priviledge"));

            } else {
                JOptionPane.showMessageDialog(null, "Record not Found");
            }
            con.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "hello" + e);
        }


    }//GEN-LAST:event_searchActionPerformed

