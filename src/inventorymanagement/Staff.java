    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
        String searchId = search_staff.getText();
        try{
           Class.forName("com.mysql.jdbc.Driver");
           Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/inventory","root","");
           
           String sql = "UPDATE staff SET staff_id =?,staff_name =?,staff_address = ?,staff_tel =?,priviledge =? WHERE staff_id ='" + searchId + "' ";
           PreparedStatement pstmt = con.prepareStatement(sql);
           
           pstmt.setInt(1,Integer.parseInt(staffId.getText()));
           pstmt.setString(2,staffName.getText());
           pstmt.setString(3,address.getText());
           pstmt.setInt(4,Integer.parseInt(telNo.getText()));
           pstmt.setString(5,privilege.getText());
          
           
           
           pstmt.executeUpdate();
           JOptionPane.showMessageDialog(null,"Successfully Updated");
           con.close();
       
       }catch(Exception e)
       {
           JOptionPane.showMessageDialog(null,e);
       }
       showTable();

    }//GEN-LAST:event_updateActionPerformed

